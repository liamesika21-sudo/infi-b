/**
 * Per-week structured summary content for the calculus-2 course.
 * Each week includes: goal, study guide, inference chains,
 * definitions, theorems, recitation notes, and practice questions.
 *
 * Math strings use LaTeX delimited by $ … $ or $$ … $$.
 */

export interface InferenceArrow {
  from: string;
  to: string;
  label?: string;
}

export interface ContentBox {
  title: string;
  /** LaTeX/Hebrew text */
  body: string;
  /** "def" | "thm" | "note" | "ex" | "warn" */
  kind?: "def" | "thm" | "note" | "ex" | "warn";
}

export interface WeekSummary {
  weekNumber: number;
  title: string;

  /* ── Section 1: Before you start ── */
  mainGoal: string;
  prereqs: string[];
  buildOn?: string;

  /* ── Section 2: Study guide ── */
  quickTip?: string;
  mustMemorize: string[];
  studySteps: string[];
  commonMistakes: string[];

  /* ── Section 3: Inference diagram ── */
  inferences: InferenceArrow[];
  importantNote?: { title: string; body: string };

  /* ── Section 4 & 5: Definitions and theorems ── */
  definitions: ContentBox[];
  theorems: ContentBox[];

  /* ── Section 6 & 7: Recitation highlights ── */
  maxSays: string[];
  practiceQuestions: string[];
}

